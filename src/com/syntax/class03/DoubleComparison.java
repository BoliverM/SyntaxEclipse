package com.syntax.class03;

public class DoubleComparison {
	public static void main(String[] args) {
		
		double x = 40;
		double y = 60;
		
		if (x > y) {
			System.out.println("Double value " + x + " is larger than " + y);
		}
		else {
			System.out.println("Value " + x + " is not larger " + y);
		}
	}

}
